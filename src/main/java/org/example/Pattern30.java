package org.example;

public class Pattern30 {
    public static void main(String[] args) {


        for (int i = 5; i >=1; i--) {
            int num=5;
            for (int j = 5; j>=1 ; j--) {

                    System.out.print(num+" ");
                    num--;
                }
            System.out.println();
        }
    }
}
